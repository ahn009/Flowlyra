SUPPORTED_LOCALES = sorted(WIDGET_CATALOGS.keys())


def normalize(locale: str | None) -> str:
    if not locale:
        return "en"
    return locale.split("-")[0].lower()


def catalog(locale: str | None) -> dict[str, str]:
    code = normalize(locale)
    return WIDGET_CATALOGS.get(code, WIDGET_CATALOGS["en"]).copy()
